
public class Calculator {
	
	public float addition(float a,float b) throws Exception {
		return a+b;
	}
	
	public float multiplication(float a,float b) throws Exception{
		return a*b;
	}
	
	public float diision(float a,float b)throws Exception {
		return a/b;
	}
	
	public float reste(float a,float b)throws Exception {
		return a%b;
		
	}
	
	public float minus(float a,float b)throws Exception {
		return a-b;
	}
	
	


}
